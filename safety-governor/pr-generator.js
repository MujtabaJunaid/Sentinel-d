"use strict";

/**
 * pr-generator.js — Safety Governor PR Generator
 *
 * Creates GitHub Pull Requests for HIGH (AUTO_PR) and MEDIUM (REVIEW_PR) tiers.
 * Uses @octokit/rest for GitHub API calls.
 */

const { Octokit } = require("@octokit/rest");
require("dotenv").config();

/**
 * Build the PR body with all required sections.
 * @param {object} candidatePatch - candidate_patch.json
 * @param {object} validationBundle - validation_bundle.json
 * @param {number} compositeScore - Composite confidence score
 * @param {string} tier - HIGH or MEDIUM
 * @returns {string} Markdown PR body
 */
function buildPRBody(candidatePatch, validationBundle, compositeScore, tier) {
  const scorePercent = (compositeScore * 100).toFixed(1);
  const badgeColor = tier === "HIGH" ? "brightgreen" : "yellow";
  const coverageDelta = (
    validationBundle.coverage_after - validationBundle.coverage_before
  ).toFixed(2);
  const coverageSign = coverageDelta >= 0 ? "+" : "";
  const visualStatus = validationBundle.visual_regression
    ? "⚠️ Regression detected"
    : "✅ No regression";

  const humanReviewBanner =
    tier === "MEDIUM"
      ? "\n> ⚠️ **Human review required before merge** — this PR was generated with MEDIUM confidence and requires manual approval.\n"
      : "";

  return `![Sentinel-D ${tier}](https://img.shields.io/badge/Sentinel--D-${tier}%20${scorePercent}%25-${badgeColor})

## 🛡️ Sentinel-D Automated Security Patch
${humanReviewBanner}
### CVE Summary

| Field | Value |
|-------|-------|
| **CVE ID** | \`${candidatePatch.cve_id || "N/A"}\` |
| **Severity** | \`${candidatePatch.severity || "N/A"}\` |
| **Affected Package** | \`${candidatePatch.affected_package || "N/A"}\` |

### Source

Patch generated via **${candidatePatch.source}** (model: \`${candidatePatch.model_id}\`)

### Sandbox Results

| Metric | Value |
|--------|-------|
| **Tests Passed** | ${validationBundle.tests_passed} |
| **Tests Failed** | ${validationBundle.tests_failed} |
| **Coverage Delta** | ${coverageSign}${coverageDelta}% |
| **Visual Regression** | ${visualStatus} |

### Confidence Breakdown

| Signal | Value |
|--------|-------|
| **Tier** | ${tier} |
| **Composite Score** | ${compositeScore.toFixed(2)} |
| **LLM Confidence** | ${candidatePatch.llm_confidence?.toFixed(2) || "N/A"} |

### Links

- 📋 [Sandbox Test Log](${validationBundle.test_log_url || "#"})
- 🧠 [LLM Reasoning Chain](${candidatePatch.reasoning_chain || "#"})
${validationBundle.screenshot_diff_url ? `- 🖼️ [Visual Diff](${validationBundle.screenshot_diff_url})` : ""}

---

*Generated by Sentinel-D v3.0 at ${new Date().toISOString()}*`;
}

/**
 * Create a Pull Request for a validated patch.
 *
 * @param {object} candidatePatch - candidate_patch.json
 * @param {object} validationBundle - validation_bundle.json
 * @param {number} compositeScore - Composite confidence score
 * @param {string} tier - "HIGH" or "MEDIUM"
 * @returns {Promise<{prNumber: number, prUrl: string}>}
 */
async function createPR(candidatePatch, validationBundle, compositeScore, tier) {
  const GITHUB_TOKEN = process.env.GITHUB_TOKEN;
  const GITHUB_OWNER = process.env.GITHUB_OWNER;
  const GITHUB_REPO = process.env.GITHUB_REPO;

  if (!GITHUB_TOKEN) {
    throw new Error("GITHUB_TOKEN environment variable is required");
  }
  if (!GITHUB_OWNER || !GITHUB_REPO) {
    throw new Error("GITHUB_OWNER and GITHUB_REPO environment variables are required");
  }

  const octokit = new Octokit({ auth: GITHUB_TOKEN });
  const cveId = candidatePatch.cve_id || candidatePatch.event_id;
  const packageName = candidatePatch.affected_package || "unknown";

  const title = `[Sentinel-D] Fix ${cveId} in ${packageName} (${tier} confidence)`;
  const body = buildPRBody(candidatePatch, validationBundle, compositeScore, tier);
  const branchName = `sentinel-d/${cveId}-${candidatePatch.event_id}`.replace(
    /[^a-zA-Z0-9\-\/]/g,
    "-"
  );

  const labels =
    tier === "HIGH"
      ? ["sentinel/auto-approve"]
      : ["sentinel/needs-review"];

  const { data: pr } = await octokit.rest.pulls.create({
    owner: GITHUB_OWNER,
    repo: GITHUB_REPO,
    title,
    body,
    head: branchName,
    base: "main",
  });

  // Add labels
  await octokit.rest.issues.addLabels({
    owner: GITHUB_OWNER,
    repo: GITHUB_REPO,
    issue_number: pr.number,
    labels,
  });

  // Enable auto-merge for HIGH tier only
  if (tier === "HIGH") {
    try {
      await octokit.graphql(`
        mutation EnableAutoMerge($pullRequestId: ID!) {
          enablePullRequestAutoMerge(input: { pullRequestId: $pullRequestId, mergeMethod: SQUASH }) {
            clientMutationId
          }
        }
      `, { pullRequestId: pr.node_id });
    } catch {
      // Auto-merge may not be available if branch protection doesn't allow it
      console.log(
        JSON.stringify({
          message: "Auto-merge not enabled (branch protection may not allow)",
          prNumber: pr.number,
        })
      );
    }
  }

  return {
    prNumber: pr.number,
    prUrl: pr.html_url,
  };
}

module.exports = { createPR, buildPRBody };
